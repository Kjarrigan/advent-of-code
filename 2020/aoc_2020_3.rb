class Array
  def x; self[0]; end
  def x=(v); self[0] = v; end
  def y; self[1]; end
  def y=(v); self[1] = v; end
  def to_s; [x, y].join('|'); end
end

class Sloper
  def initialize(x, y)
    @x, @y = x, y
  end

  def run!
    pos = [0, 0]
    trees_hit = 0

    until pos.y == (MAP.size - 1)
    begin
      pos.x = (pos.x + @x) % WIDTH
      pos.y += @y
      trees_hit += 1 if MAP[pos.y][pos.x] == '#'
    end
    end
    return trees_hit
  end
end

