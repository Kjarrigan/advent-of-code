# the hexgrid has the pointy end to the north

require 'matrix'

BLACK = 1
WHITE = 0

def input_to_coord(path)
  ref = Vector[0,0]

  until path.empty?
    case path
    when /^ne/
      ref += Vector[0,-1]
      path.slice!(0,2)
    when /^se/
      ref += Vector[1,1]
      path.slice!(0,2)
    when /^nw/
      ref += Vector[-1,-1]
      path.slice!(0,2)
    when /^sw/
      ref += Vector[0,1]
      path.slice!(0,2)
    when /^e/
      ref += Vector[1,0]
      path.slice!(0,1)
    when /^w/
      ref += Vector[-1,0]
      path.slice!(0,1)
    else
      raise ArgumentError, "Unknown direction: #{path.inspect}"
    end
  end

  ref
end

def black_neighbors(grid, pos)
  neigh = 0
  neigh += 1 if grid[pos + Vector[0,-1] ] == BLACK
  neigh += 1 if grid[pos + Vector[1,1]  ] == BLACK
  neigh += 1 if grid[pos + Vector[-1,-1]] == BLACK
  neigh += 1 if grid[pos + Vector[0,1]  ] == BLACK
  neigh += 1 if grid[pos + Vector[1,0]  ] == BLACK
  neigh += 1 if grid[pos + Vector[-1,0] ] == BLACK
  neigh
end

def turn(grid)
  new_grid = Hash.new(0)

  min_x = grid.keys.map{|k| k[0] }.min - 1
  max_x = grid.keys.map{|k| k[0] }.max + 1
  min_y = grid.keys.map{|k| k[1] }.min - 1
  max_y = grid.keys.map{|k| k[1] }.max + 1
  (min_x..max_x).each do |x|
    (min_y..max_y).each do |y|
      pos = Vector[x,y]
      color = grid[pos]
      neigh = black_neighbors(grid, pos)
      if color == WHITE && neigh == 2
        new_grid[pos] = BLACK
      elsif color == BLACK && (neigh.zero? || neigh > 2)
        new_grid[pos] = WHITE
      else
        new_grid[pos] = color
      end
    end
  end

  new_grid
end

grid = Hash.new(0)

DATA.each do |input|
  grid[input_to_coord(input.chomp)] += 1
end

grid = grid.map {|k ,v| [k, v % 2] }.to_h
grid.default = 0

print "Part 1: "
puts grid.values.count(1)

binding.irb
# p grid
(1..100).each do |day|
  grid = turn(grid)
  print "Day #{day}: "
  puts grid.values.count(1)
end

